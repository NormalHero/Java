
public class Test1 { // 코드에 대한 설명
	
	public static void main(String[] args) {
		
		System.out.println("출력할 문장입력");
		// 줄바꿈을 제공o
		// syso작성후, ctrl+space bar -> Enter
		
		System.out.print("8일동안 열심히해봐요~^^!!");
		// 줄바꿈을 제공xxx
		
		
		// 데이터의 type을 구분
		//  -> 자료형
		int i=10;
		double d=3.14;
		char c='a';
		String str="APPLE";
		Boolean bool=false;
		
		// 변수: 데이터를 저장하는 공간
		int i2;
		i2=10; // 대입 연산자(10=i2;xxx)
		
		System.out.println();
		System.out.println();
		
		bool=true;
		
		System.out.println("i= "+i);
		System.out.println("d= "+d);
		System.out.println("c= "+c);
		System.out.println("str= "+str);
		System.out.println("bool= "+bool);
		
		
		
	}
	
}


